# Integer 
integer_variable = 100
# Floating-point 
float_variable = 3.14
# String 
string_variable = "Hello, Python!"
# Boolean 
boolean_variable = True
# List 
list_variable = [1, "apple", 3.5]
# Tuple 
tuple_variable = (1, "banana", 4.0)
# Dictionary 
dict_variable = {"name": "MR:A.K", "stream": "BCA"}
# Set 
set_variable = {1, 2, 3, 3, 4} # Note: the duplicate '3' will be removed



print("--- Displaying Variable Values and Their Types ---")


print(f"Variable: {integer_variable}, \t Type: {type(integer_variable)}")
print(f"Variable: {float_variable}, \t\t Type: {type(float_variable)}")
print(f"Variable: '{string_variable}', \t Type: {type(string_variable)}")
print(f"Variable: {boolean_variable}, \t\t Type: {type(boolean_variable)}")
print(f"Variable: {list_variable}, \t Type: {type(list_variable)}")
print(f"Variable: {tuple_variable}, \t Type: {type(tuple_variable)}")
print(f"Variable: {dict_variable}, \t Type: {type(dict_variable)}")
print(f"Variable: {set_variable}, \t\t Type: {type(set_variable)}")